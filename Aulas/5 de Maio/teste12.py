import string

'''Um exemplo é a constante string.digits é equivalente a
“0123456789”, que pode ser usada para verificar se um
caractere é um dígito usando o operador in

A constante string.ascii_lowercase contém todas as letras ASCII
que o sistema considera serem minúsculas

Da mesma forma, string.ascii_uppercase contém todas as letras
maiúsculas e string.punctuation contém todos os caracteres
considerados símbolos de pontuação'''

print(string.ascii_lowercase)
print(string.ascii_uppercase)
print(string.digits)
print(string.punctuation)